"""Operator	Name	                    Example
==	        Equal	                    x == y	
!=	        Not equal	                x != y	
>	        Greater than	            x > y	
<	        Less than	                x < y	
>=	        Greater than or equal to	x >= y	
<=	        Less than or equal to	    x <= y"""

a = 10
b = 12
if a == b:
    print("yes!")
else:
    print("no")
if a != b:
    print("True")
if a > b or a < b:
    print("True atleast one condition has been satisfied")
